using VerifyCS =
    Converg.Generator.Tests.CSharpSourceGeneratorVerifier<Converg.Generator.FluentFactoryGenerator>;

namespace Converg.Generator.Tests;

public class OverlappingMethodNameTests
{
    private const string SourceFile = "Source.cs";

    /// <summary>
    /// Tests that two parameters on the same constructor both annotated with the same
    /// <c>[FluentMethod("WithValue")]</c> name but of different types do not produce a collision,
    /// because the generated methods appear on different sequential step structs.
    /// </summary>
    [Fact]
    internal async Task Given_single_constructor_with_two_params_sharing_a_FluentMethod_name_Should_generate_sequential_steps_with_same_method_name()
    {
        const string code =
            """
            using Converg.Generator;

            namespace Test;

            [FluentFactory]
            public static partial class Factory;

            public class MyBuildTarget
            {
                [FluentConstructor(typeof(Factory), CreateMethod = CreateMethod.None)]
                public MyBuildTarget([FluentMethod("WithValue")]string text, [FluentMethod("WithValue")]int number)
                {
                    Text = text;
                    Number = number;
                }

                public string Text { get; set; }
                public int Number { get; set; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converg", "1.0.0.0")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.MyBuildTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_0__Test_Factory WithValue(in string text)
                    {
                        return new global::Test.Step_0__Test_Factory(text);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.MyBuildTarget"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converg", "1.0.0.0")]
                public readonly struct Step_0__Test_Factory
                {
                    private readonly string _text__parameter;
                    internal Step_0__Test_Factory(in string text)
                    {
                        this._text__parameter = text;
                    }

                    /// <summary>
                    ///     <seealso cref="Test.MyBuildTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.MyBuildTarget WithValue(in int number)
                    {
                        return new global::Test.MyBuildTarget(this._text__parameter, number);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { (SourceFile, code) },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }

    /// <summary>
    /// Tests that two constructors on separate target classes pointing at the same factory,
    /// where the first parameters share the same <c>[FluentMethod("Create")]</c> name but have
    /// different types (<c>string</c> vs <c>int</c>), produce valid C# overloaded methods on the factory.
    /// </summary>
    [Fact]
    internal async Task Given_two_constructors_with_same_FluentMethod_name_but_different_types_Should_generate_overloaded_factory_methods()
    {
        const string code =
            """
            using Converg.Generator;

            namespace Test;

            [FluentFactory]
            public static partial class Factory;

            public class TargetA
            {
                [FluentConstructor(typeof(Factory), CreateMethod = CreateMethod.None)]
                public TargetA([FluentMethod("Create")]string value)
                {
                    Value = value;
                }

                public string Value { get; set; }
            }

            public class TargetB
            {
                [FluentConstructor(typeof(Factory), CreateMethod = CreateMethod.None)]
                public TargetB([FluentMethod("Create")]int value)
                {
                    Value = value;
                }

                public int Value { get; set; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converg", "1.0.0.0")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.TargetA"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.TargetA Create(in string value)
                    {
                        return new global::Test.TargetA(value);
                    }

                    /// <summary>
                    ///     <seealso cref="Test.TargetB"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.TargetB Create(in int value)
                    {
                        return new global::Test.TargetB(value);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { (SourceFile, code) },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }
}
