using VerifyCS =
    Motiv.FluentFactory.Generator.Tests.CSharpSourceGeneratorVerifier<Motiv.FluentFactory.Generator.FluentFactoryGenerator>;

namespace Motiv.FluentFactory.Generator.Tests;

public class FluentFactoryGeneratorDeepNestedGenericTests
{
    /// <summary>
    /// Verifies that a 3-level non-delegate generic nesting (Dictionary&lt;string, List&lt;KeyValuePair&lt;T, U&gt;&gt;&gt;)
    /// generates correct fluent method with full global:: qualification at every nesting level.
    /// Complements the existing delegate-based deep nesting tests.
    /// </summary>
    [Fact]
    internal async Task Should_generate_when_applied_to_a_class_constructor_with_three_level_non_delegate_generic_nesting()
    {
        const string code =
            """
            using System.Collections.Generic;
            using Motiv.FluentFactory.Generator;

            namespace Test;

            [FluentFactory]
            public static partial class Factory;

            public class MyBuildTarget<T, U>
            {
                [FluentConstructor(typeof(Factory), Options = FluentOptions.NoCreateMethod)]
                public MyBuildTarget(Dictionary<string, List<KeyValuePair<T, U>>> value)
                {
                    Value = value;
                }

                public Dictionary<string, List<KeyValuePair<T, U>>> Value { get; set; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Motiv.FluentFactory", "1.0.0.0")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.MyBuildTarget{T, U}"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.MyBuildTarget<T, U> WithValue<T, U>(in global::System.Collections.Generic.Dictionary<string, global::System.Collections.Generic.List<global::System.Collections.Generic.KeyValuePair<T, U>>> value)
                    {
                        return new global::Test.MyBuildTarget<T, U>(value);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }

    /// <summary>
    /// Verifies that a 4-level non-delegate generic nesting
    /// (IReadOnlyDictionary&lt;string, IEnumerable&lt;Tuple&lt;T, List&lt;U&gt;&gt;&gt;&gt;) generates correct fluent method
    /// with full global:: qualification at every nesting level.
    /// </summary>
    [Fact]
