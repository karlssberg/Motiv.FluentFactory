                    ///     <seealso cref="Test.MyBuildTarget{T, U}"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.MyBuildTarget<T, U> WithValue<T, U>(in global::System.Collections.Generic.IReadOnlyDictionary<string, global::System.Collections.Generic.IEnumerable<global::System.Tuple<T, global::System.Collections.Generic.List<U>>>> value)
                    {
                        return new global::Test.MyBuildTarget<T, U>(value);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }
}
