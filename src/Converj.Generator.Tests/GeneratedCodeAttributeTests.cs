using VerifyCS =
    Converj.Generator.Tests.CSharpSourceGeneratorVerifier<Converj.Generator.FluentRootGenerator>;

namespace Converj.Generator.Tests;

public class GeneratedCodeAttributeTests
{
    [Fact]
    internal async Task Should_add_GeneratedCode_attribute_to_root_type_and_step_struct()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentRoot]
            public static partial class Builder;

            public class MyTarget
            {
                [FluentTarget(typeof(Builder))]
                public MyTarget(int value)
                {
                    Value = value;
                }
                public int Value { get; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public static partial class Builder
                {
                    /// <summary>
                    ///     <seealso cref="Test.MyTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_0__Test_Builder WithValue(in int value)
                    {
                        return new global::Test.Step_0__Test_Builder(value);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.MyTarget"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_0__Test_Builder
                {
                    private readonly int _value__parameter;
                    internal Step_0__Test_Builder(in int value)
                    {
                        this._value__parameter = value;
                    }

                    /// <summary>
                    /// Creates a new instance using constructor Test.MyTarget.MyTarget(int value).
                    ///
                    ///     <seealso cref="Test.MyTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.MyTarget CreateMyTarget()
                    {
                        return new global::Test.MyTarget(this._value__parameter);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentRootGenerator), "Test.Builder.g.cs", expected)
                }
            }
        }.RunAsync();
    }

    [Fact]
    internal void Should_have_non_empty_well_formed_version_string()
    {
        var version = typeof(FluentRootGenerator).Assembly.GetName().Version?.ToString();
        Assert.NotNull(version);
        Assert.NotEmpty(version);
        Assert.Matches(@"^\d+\.\d+\.\d+(\.\d+)?$", version);
    }
}
