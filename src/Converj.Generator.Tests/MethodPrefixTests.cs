    [Fact]
    internal async Task Should_report_CVJG0018_for_invalid_MethodPrefix_on_factory()
    {
        const string code =
            """
            using Converj.Generator;

            namespace Test;

            [FluentRoot(MethodPrefix = "123Bad")]
            public static partial class Factory;

            public class Alpha
            {
                [FluentTarget(typeof(Factory))]
                public Alpha(int value)
                {
                    Value = value;
                }

                public int Value { get; set; }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { (SourceFile, code) },
                ExpectedDiagnostics =
                {
                    DiagnosticResult.CompilerError(InvalidMethodPrefix.Id)
                        .WithSpan(SourceFile, 5, 2, 5, 37)
                        .WithMessage("MethodPrefix must be a valid identifier")
                }
            }
        }.RunAsync();
    }

