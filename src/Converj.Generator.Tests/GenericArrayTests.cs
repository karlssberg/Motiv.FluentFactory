using VerifyCS =
    Converj.Generator.Tests.CSharpSourceGeneratorVerifier<Converj.Generator.FluentFactoryGenerator>;

namespace Converj.Generator.Tests;

public class GenericArrayTests
{
    /// <summary>
    /// Verifies that a simple generic array parameter (T[]) generates correct fluent method and step
    /// struct using array syntax in the type signature.
    /// </summary>
    [Fact]
    internal async Task Should_generate_when_applied_to_a_class_constructor_with_a_generic_array_parameter()
    {
        const string code =
            """
            using Converj.Generator;

            namespace Test;

            [FluentFactory]
            public static partial class Factory;

            public class MyBuildTarget<T>
            {
                [FluentConstructor(typeof(Factory), CreateMethod = CreateMethod.None)]
                public MyBuildTarget(T[] values)
                {
                    Values = values;
                }

                public T[] Values { get; set; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "1.0.0.0")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.MyBuildTarget{T}"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.MyBuildTarget<T> WithValues<T>(in T[] values)
                    {
                        return new global::Test.MyBuildTarget<T>(values);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }

