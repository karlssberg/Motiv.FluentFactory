    /// <summary>
    /// Tests that the generator uses the explicit constructor parameters when a record has both positional parameters
    /// (synthesized primary constructor) and an explicit constructor annotated with [FluentTarget].
    /// The generator should use the 3-parameter explicit constructor (name, age, email) rather than
    /// the 2-parameter synthesized one from the positional parameters.
    /// </summary>
    [Fact]
    internal async Task Should_generate_from_explicit_constructor_when_record_has_both_positional_and_explicit_constructor()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentRoot]
            public static partial class Factory;

            public record MyBuildTarget(string Name, int Age)
            {
                [FluentTarget(typeof(Factory), TerminalMethod = TerminalMethod.None)]
                public MyBuildTarget(string name, int age, string email) : this(name, age)
                {
                    Email = email;
                }

                public string Email { get; init; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.MyBuildTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_0__Test_Factory WithName(in string name)
                    {
                        return new global::Test.Step_0__Test_Factory(name);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.MyBuildTarget"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_0__Test_Factory
                {
                    private readonly string _name__parameter;
                    internal Step_0__Test_Factory(in string name)
                    {
                        this._name__parameter = name;
                    }

                    /// <summary>
                    ///     <seealso cref="Test.MyBuildTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.Step_1__Test_Factory WithAge(in int age)
                    {
                        return new global::Test.Step_1__Test_Factory(this._name__parameter, age);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.MyBuildTarget"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_1__Test_Factory
                {
