using VerifyCS =
    Converj.Generator.Tests.CSharpSourceGeneratorVerifier<Converj.Generator.FluentFactoryGenerator>;

namespace Converj.Generator.Tests;

public class TypeFirstTests
{
    [Fact]
    internal async Task Should_generate_type_first_entry_method_for_single_constructor_with_one_parameter()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentFactory(BuilderMode = BuilderMode.TypeFirst)]
            public static partial class Factory;

            public class Dog
            {
                [FluentConstructor(typeof(Factory))]
                public Dog(string name)
                {
                    Name = name;
                }

                public string Name { get; set; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.Dog"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_Dog_0__Test_Factory BuildDog()
                    {
                        return new global::Test.Step_Dog_0__Test_Factory();
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.Dog"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_Dog_0__Test_Factory
                {
                    public Step_Dog_0__Test_Factory()
                    {
                    }

                    /// <summary>
                    ///     <seealso cref="Test.Dog"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.Step_Dog_1__Test_Factory WithName(in string name)
                    {
                        return new global::Test.Step_Dog_1__Test_Factory(name);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.Dog"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_Dog_1__Test_Factory
                {
                    private readonly string _name__parameter;
                    internal Step_Dog_1__Test_Factory(in string name)
                    {
                        this._name__parameter = name;
                    }

                    /// <summary>
                    /// Creates a new instance using constructor Test.Dog.Dog(string name).
                    ///
                    ///     <seealso cref="Test.Dog"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.Dog Create()
                    {
                        return new global::Test.Dog(this._name__parameter);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }

    [Fact]
    internal async Task Should_generate_separate_entry_methods_for_different_target_types()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentFactory(BuilderMode = BuilderMode.TypeFirst)]
            public static partial class Factory;

            public class Dog
            {
                [FluentConstructor(typeof(Factory))]
                public Dog(string name)
                {
                    Name = name;
                }

                public string Name { get; set; }
            }

            public class Cat
            {
                [FluentConstructor(typeof(Factory))]
                public Cat(int lives)
                {
                    Lives = lives;
                }

                public int Lives { get; set; }
            }
            """;

        // Verify BuildDog and BuildCat both appear, no parameter-first chain
        var compilation = await RoslynTestHelper.GetGeneratedCompilationAsync(code);
        var generatedSource = RoslynTestHelper.GetGeneratedSource(compilation);

        Assert.Contains("BuildDog", generatedSource);
        Assert.Contains("BuildCat", generatedSource);
        Assert.DoesNotContain("CreateDog", generatedSource);
        Assert.DoesNotContain("CreateCat", generatedSource);
        Assert.Contains("Step_Dog_0", generatedSource);
        Assert.Contains("Step_Cat_", generatedSource);
    }

    [Fact]
    internal async Task Should_merge_generic_and_non_generic_same_name_under_one_entry()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentFactory(BuilderMode = BuilderMode.TypeFirst)]
            public static partial class Factory;

            public class Circle
            {
                [FluentConstructor(typeof(Factory))]
                public Circle(double radius)
                {
                    Radius = radius;
                }

                public double Radius { get; set; }
            }

            public class Circle<T>
            {
                [FluentConstructor(typeof(Factory))]
                public Circle(T radius)
                {
                    Radius = radius;
                }

                public T Radius { get; set; }
            }
            """;

        // Verify BuildCircle is the only entry point (no parameter-first WithRadius root methods)
        // and that both Circle and Circle<T> are reachable through the type-first chain
        var compilation = await RoslynTestHelper.GetGeneratedCompilationAsync(code);
        var generatedSource = RoslynTestHelper.GetGeneratedSource(compilation);

        Assert.Contains("BuildCircle", generatedSource);
        Assert.DoesNotContain("CreateCircle", generatedSource);
        // Both WithRadius(double) and WithRadius<T> should appear on the type-first root step
        Assert.Contains("WithRadius(in double radius)", generatedSource);
        Assert.Contains("WithRadius<T>(in T radius)", generatedSource);
        Assert.Contains("Step_Circle_0", generatedSource);
    }

    [Fact]
    internal async Task Should_allow_per_constructor_type_first_override()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentFactory]
            public static partial class Factory;

            public class Dog
            {
                [FluentConstructor(typeof(Factory), BuilderMode = BuilderMode.TypeFirst)]
                public Dog(string name)
                {
                    Name = name;
                }

                public string Name { get; set; }
            }

            public class Cat
            {
                [FluentConstructor(typeof(Factory))]
                public Cat(int lives)
                {
                    Lives = lives;
                }

                public int Lives { get; set; }
            }
            """;

        // Dog has TypeFirst enabled at constructor level, Cat does not.
        // Only BuildDog() should appear, not BuildCat().
        // Verify via Roslyn compilation that the generated code compiles and contains BuildDog.
        var compilation = await RoslynTestHelper.GetGeneratedCompilationAsync(code);
        var generatedSource = RoslynTestHelper.GetGeneratedSource(compilation);

        Assert.Contains("BuildDog", generatedSource);
        Assert.DoesNotContain("BuildCat", generatedSource);
    }

    [Fact]
    internal async Task Should_use_custom_type_first_verb()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentFactory(BuilderMode = BuilderMode.TypeFirst, TypeFirstVerb = "Make")]
            public static partial class Factory;

            public class Dog
            {
                [FluentConstructor(typeof(Factory))]
                public Dog(string name)
                {
                    Name = name;
                }

                public string Name { get; set; }
            }
            """;

        // Entry method should be MakeDog() instead of BuildDog()
        var compilation = await RoslynTestHelper.GetGeneratedCompilationAsync(code);
        var generatedSource = RoslynTestHelper.GetGeneratedSource(compilation);

        Assert.Contains("MakeDog", generatedSource);
        Assert.DoesNotContain("BuildDog", generatedSource);
    }

    [Fact]
    internal async Task Should_emit_diagnostic_when_type_first_entry_methods_collide()
    {
        const string code =
            """
            using System;
            using Converj.Generator;

            namespace Test;

            [FluentFactory(BuilderMode = BuilderMode.TypeFirst)]
            public static partial class Factory;

            namespace A
            {
                public class Circle
                {
                    [FluentConstructor(typeof(Test.Factory))]
                    public Circle(double radius)
                    {
                        Radius = radius;
                    }

                    public double Radius { get; set; }
                }
            }

            namespace B
            {
                public class Circle
                {
                    [FluentConstructor(typeof(Test.Factory))]
                    public Circle(int radius)
                    {
                        Radius = radius;
                    }

                    public int Radius { get; set; }
                }
            }
            """;

        // Both A.Circle and B.Circle would produce BuildCircle() — should emit diagnostic
        var compilation = await RoslynTestHelper.GetGeneratedCompilationAsync(code);
        var generatedSource = RoslynTestHelper.GetGeneratedSource(compilation);

        // The colliding entry methods should NOT be generated
        Assert.DoesNotContain("BuildCircle", generatedSource);
    }
}
