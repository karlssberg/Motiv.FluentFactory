using Microsoft.CodeAnalysis.Testing;
using static Converj.Generator.Diagnostics.FluentDiagnostics;
using VerifyCS =
    Converj.Generator.Tests.CSharpSourceGeneratorVerifier<Converj.Generator.FluentRootGenerator>;

namespace Converj.Generator.Tests;

/// <summary>
/// Tests for constructor scope and accessibility edge cases.
/// Private or protected constructors with [FluentTarget] cannot be called from generated code.
/// Builder root types missing the partial modifier cannot receive generated methods.
/// </summary>
public class ScopeAndAccessibilityTests
{
    private const string SourceFile = "Source.cs";

    /// <summary>
    /// A private constructor with [FluentTarget] cannot be used in a fluent factory
    /// because the generated code cannot call it. The generator should emit CVJG0012
    /// warning and skip generation for that constructor.
    /// </summary>
    [Fact]
    internal async Task Should_emit_diagnostic_when_FluentTarget_applied_to_private_constructor()
    {
        const string code =
            """
            using Converj.Generator;

            namespace Test;

            [FluentRoot]
            public static partial class Builder;

            public class MyTarget
            {
                [FluentTarget(typeof(Builder))]
                private MyTarget(int value)
                {
                    Value = value;
                }

                public int Value { get; set; }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { (SourceFile, code) },
                ExpectedDiagnostics =
                {
                    DiagnosticResult.CompilerWarning(InaccessibleConstructor.Id)
                        .WithSpan(SourceFile, 11, 13, 11, 21)
                        .WithArguments("Test.MyTarget.MyTarget(int)", "Private")
                }
            }
        }.RunAsync();
    }

