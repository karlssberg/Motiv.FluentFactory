    /// <summary>
    /// A nested private class with [FluentTarget] pointing to an outer public factory.
    /// The private nested class is less accessible than the public factory (Private &lt; Public),
    /// so CVJG0015 (accessibility mismatch) should fire.
    /// This test documents the generator's behavior for nested private classes as factory targets.
    /// </summary>
    [Fact]
    internal async Task Should_handle_nested_private_class_as_root_target()
    {
        const string code =
            """
            using Converj.Generator;

            namespace Test;

            [FluentRoot]
            public static partial class OuterBuilder;

            public class OuterContainer
            {
                private class NestedTarget
                {
                    [FluentTarget(typeof(OuterBuilder))]
                    public NestedTarget(int value)
                    {
                        Value = value;
                    }

                    public int Value { get; set; }
                }
            }
            """;

        // The generator still produces output for the nested private class (CVJG0015 is a Warning,
        // not an Error). The generated code calls new OuterContainer.NestedTarget(...) which
        // will fail at compile time due to private access — this is a known limitation documented here.
        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public static partial class OuterBuilder
                {
                    /// <summary>
                    ///     <seealso cref="Test.OuterContainer.NestedTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_0__Test_OuterBuilder WithValue(in int value)
                    {
                        return new global::Test.Step_0__Test_OuterBuilder(value);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.OuterContainer.NestedTarget"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_0__Test_OuterBuilder
                {
                    private readonly int _value__parameter;
                    internal Step_0__Test_OuterBuilder(in int value)
                    {
                        this._value__parameter = value;
                    }

                    /// <summary>
                    /// Creates a new instance using constructor Test.OuterContainer.NestedTarget.NestedTarget(int value).
                    ///
                    ///     <seealso cref="Test.OuterContainer.NestedTarget"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.OuterContainer.NestedTarget CreateOuterContainer_NestedTarget()
                    {
                        return new global::Test.OuterContainer.NestedTarget(this._value__parameter);
                    }
                }
            }
            """;

        // CVJG0015 fires because the nested private class (Private) is less accessible
        // than the public factory (Public). This documents the generator's behavior:
        // the diagnostic fires as a warning but generation still proceeds (shortcoming).
        await new VerifyCS.Test
        {
            CompilerDiagnostics = Microsoft.CodeAnalysis.Testing.CompilerDiagnostics.None,
            TestState =
            {
                Sources = { (SourceFile, code) },
                GeneratedSources = { (typeof(FluentRootGenerator), "Test.OuterBuilder.g.cs", expected) },
                ExpectedDiagnostics =
                {
                    DiagnosticResult.CompilerWarning(AccessibilityMismatch.Id)
                        .WithSpan(SourceFile, 13, 16, 13, 28)
                        .WithArguments("Test.OuterBuilder", "Public", "Test.OuterContainer.NestedTarget", "Private")
                }
            }
        }.RunAsync();
    }

    /// <summary>
    /// A factory root type without the <c>partial</c> modifier cannot receive generated fluent methods.
