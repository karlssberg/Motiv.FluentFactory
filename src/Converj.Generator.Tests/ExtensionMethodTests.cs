using VerifyCS =
    Converj.Generator.Tests.CSharpSourceGeneratorVerifier<Converj.Generator.FluentFactoryGenerator>;

namespace Converj.Generator.Tests;

public class ExtensionMethodTests
{
    [Fact]
    internal async Task Should_generate_extension_method_chain_for_this_parameter()
    {
        const string code =
            """
            using System;
            using Converj.Attributes;

            namespace Test;

            [FluentRoot]
            public static partial class StringExt;

            public static class StringHelpers
            {
                [FluentTarget(typeof(StringExt))]
                public static string Pad(this string input, int width, char padChar) =>
                    input.PadRight(width, padChar);
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public static partial class StringExt
                {
