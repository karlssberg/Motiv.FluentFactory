                    ///     <seealso cref="Test.StringHelpers"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_0__Test_StringExt WithWidth(this string input, in int width)
                    {
                        return new global::Test.Step_0__Test_StringExt(input, width);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.StringHelpers"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_0__Test_StringExt
                {
                    private readonly string _input__parameter;
                    private readonly int _width__parameter;
                    internal Step_0__Test_StringExt(in string input, in int width)
                    {
                        this._input__parameter = input;
                        this._width__parameter = width;
                    }

                    /// <summary>
                    ///     <seealso cref="Test.StringHelpers"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.Step_1__Test_StringExt WithPadChar(in char padChar)
                    {
                        return new global::Test.Step_1__Test_StringExt(this._input__parameter, this._width__parameter, padChar);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.StringHelpers"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_1__Test_StringExt
                {
                    private readonly string _input__parameter;
                    private readonly int _width__parameter;
                    private readonly char _padChar__parameter;
                    internal Step_1__Test_StringExt(in string input, in int width, in char padChar)
                    {
                        this._input__parameter = input;
                        this._width__parameter = width;
                        this._padChar__parameter = padChar;
                    }

                    /// <summary>
                    /// Calls static method string Test.StringHelpers.Pad(string input, int width, char padChar).
                    ///
                    ///     <seealso cref="Test.StringHelpers"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public string Pad()
                    {
                        return global::Test.StringHelpers.Pad(this._input__parameter, this._width__parameter, this._padChar__parameter);
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.StringExt.g.cs", expected)
                }
            }
        }.RunAsync();
    }
}
