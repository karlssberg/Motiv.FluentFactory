    [Fact]
    internal async Task Should_unpack_named_tuple_parameter_into_multi_param_fluent_method()
    {
        const string code =
            """
            using System;
            using Converj.Attributes;

            namespace Test;

            [FluentRoot]
            public static partial class Factory;

            public class Vehicle
            {
                [FluentTarget(typeof(Factory))]
                public Vehicle((int Wheels, string Name) vehicle)
                {
                    Vehicle1 = vehicle;
                }

                public (int Wheels, string Name) Vehicle1 { get; set; }
            }
            """;

        const string expected =
            """
            // <auto-generated/>
            #nullable enable
            namespace Test
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public static partial class Factory
                {
                    /// <summary>
                    ///     <seealso cref="Test.Vehicle"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public static global::Test.Step_0__Test_Factory WithVehicle(in int wheels, in string name)
                    {
                        return new global::Test.Step_0__Test_Factory(wheels, name);
                    }
                }

                /// <summary>
                ///     <seealso cref="Test.Vehicle"/>
                /// </summary>
                [global::System.CodeDom.Compiler.GeneratedCode("Converj", "$$VERSION$$")]
                public readonly struct Step_0__Test_Factory
                {
                    private readonly int _wheels__parameter;
                    private readonly string _name__parameter;
                    internal Step_0__Test_Factory(in int wheels, in string name)
                    {
                        this._wheels__parameter = wheels;
                        this._name__parameter = name;
                    }

                    /// <summary>
                    /// Creates a new instance using constructor Test.Vehicle.Vehicle((int Wheels, string Name) vehicle).
                    ///
                    ///     <seealso cref="Test.Vehicle"/>
                    /// </summary>
                    [global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]
                    public global::Test.Vehicle CreateVehicle()
                    {
                        return new global::Test.Vehicle((this._wheels__parameter, this._name__parameter));
                    }
                }
            }
            """;

        await new VerifyCS.Test
        {
            TestState =
            {
                Sources = { code },
                GeneratedSources =
                {
                    (typeof(FluentFactoryGenerator), "Test.Factory.g.cs", expected)
                }
            }
        }.RunAsync();
    }

